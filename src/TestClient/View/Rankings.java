    public void setDataRanking(ArrayList<Player> leaderBoard) {
        ArrayList<Object[]> data = new ArrayList<>();
        for(int i = 0; i < leaderBoard.size(); i++) {
            String name = leaderBoard.get(i).getNickName();
            int matchNum = leaderBoard.get(i).getNumberOfGame();
            String winRate = leaderBoard.get(i).getWinRate();
            double score = leaderBoard.get(i).getScore();
            int rank = i + 1;
            data.add(new Object[]{name, matchNum, winRate, score, rank});
        }
        for (Object[] row : data) {
            tblModel.addRow(row);
        }
    }
